var minimizeTheDifference = function (mat, target) {
    let minDifference = Infinity;
    let poss = new Array(mat[0].length).fill(0);
    for (let rowIndex = mat.length - 1; rowIndex >= 0; rowIndex--) {
        const newPoss = {};
        for (let columnIndex = 0; columnIndex < mat[rowIndex].length; columnIndex++) {
            for (let possIndex = 0; possIndex < poss.length; possIndex++) {
                newPoss[Number(poss[possIndex]) + mat[rowIndex][columnIndex]] = true;
            }
        }
        poss = Object.keys(newPoss);
    }

    const bucketSort = (function (queries) {
        const maxNumber = Math.max(...queries);
        const arr = new Array(maxNumber);
        for (let index = 0; index < queries.length; index++) {
            if (arr[queries[index]] === undefined) {
                arr[queries[index]] = 0;
            }
            arr[queries[index]] += 1;
        }
        let sortedArray = [];
        for (let index = 0; index < arr.length; index++) {
            for (let indexTemp = 0; indexTemp < arr[index]; indexTemp++) {
                sortedArray.push(index);
            }
        }
        return sortedArray;
    })(poss);

    function binarySearch(arr, search, index = 0) {
        if (arr.length === 1) {
            minDifference = Math.min(Math.abs(arr[0] - target), minDifference);
            return 0;
        }
        if (!arr.length || !search) {
            return 0;
        }
        const middleIndex = parseInt(arr.length / 2);
        minDifference = Math.min(Math.abs(arr[middleIndex] - target), minDifference);
        if (arr[middleIndex] === search) {
            return index + middleIndex;
        } else if (arr[middleIndex] < search) {
            return binarySearch(arr.slice(middleIndex), search, index + middleIndex);
        } else {
            return binarySearch(arr.slice(0, middleIndex), search, index);
        }
    }
    binarySearch(bucketSort, target);
    return minDifference;
};

