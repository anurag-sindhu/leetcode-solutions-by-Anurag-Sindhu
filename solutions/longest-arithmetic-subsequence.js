var longestArithSeqLength = function (nums) {
    function travel(diff, from, obj = {}) {
        if (obj[from] != undefined) {
            return obj[from];
        }
        let tempInput = -Infinity;
        if (diffStartEnd[diff][from] == undefined) {
            return 0;
        }
        for (const element of diffStartEnd[diff][from]) {
            tempInput = Math.max(tempInput, 1 + travel(diff, element, obj));
        }
        obj[from] = tempInput;
        return tempInput;
    }

    const diff = {};
    for (let index = nums.length - 1; index > 0; index--) {
        for (let indexChild = index - 1; indexChild >= 0; indexChild--) {
            const difference = nums[index] - nums[indexChild];
            if (!diff[difference]) {
                diff[difference] = [];
            }
            diff[difference].push([index, indexChild]);
        }
    }
    const diffStartEnd = {};
    for (const key in diff) {
        if (diffStartEnd[key] == undefined) {
            diffStartEnd[key] = {};
        }
        for (const element of diff[key]) {
            const [end, start] = element;
            if (diffStartEnd[key][start] == undefined) {
                diffStartEnd[key][start] = [];
            }
            diffStartEnd[key][start].push(end);
        }
    }
    let count = 0;
    for (const element in diff) {
        const obj = {};
        for (let ind = diff[element].length - 1; ind >= 0; ind--) {
            const iterator = diff[element][ind];
            const tempCount = travel(element, iterator[1], obj);
            count = Math.max(count, tempCount != 0 ? tempCount + 1 : 0);
        }
    }
    return count;
};

