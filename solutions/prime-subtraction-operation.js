function isPrime(num) {
    if (num <= 1) return false;
    if (num === 2) return true;
    if (num % 2 === 0) return false;

    const sqrt = Math.floor(Math.sqrt(num));
    for (let i = 3; i <= sqrt; i += 2) {
        if (num % i === 0) {
            return false;
        }
    }
    return true;
}
var primeSubOperation = function (nums) {
    const primeNumberTill = (function () {
        const num = [];
        for (let index = 2; index <= 997; index++) {
            if (isPrime(index)) {
                num.push(index);
            }
        }
        return num;
    })();
    for (let i = nums.length - 2; i >= 0; i--) {
        if (nums[i] < nums[i + 1]) {
            continue;
        }

        let target = nums[i] - nums[i + 1];
        let prime = primeNumberTill.find((p) => p > target);
        if (!prime) {
            return false;
        }

        nums[i] -= prime;
        if (nums[i] <= 0) {
            return false;
        }
    }
    return true;
};


