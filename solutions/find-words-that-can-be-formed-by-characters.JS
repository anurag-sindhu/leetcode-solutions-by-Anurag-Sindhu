function getFrequencyOfChars(word) {
	const obj = {};
	for (let index = 0; index < word.length; index++) {
		if (!obj[word[index]]) {
			obj[word[index]] = 0;
		}
		obj[word[index]] += 1;
	}
	return obj;
}

var countCharacters = function(words, chars) {
	const fre = getFrequencyOfChars(words[0]);
	const wordsFrequency = {};
	const wordsCount = {};
	const charsFrequency = getFrequencyOfChars(chars);
	for (const iterator of words) {
		wordsFrequency[iterator] = getFrequencyOfChars(iterator);
		if (!wordsCount[iterator]) {
			wordsCount[iterator] = 0;
		}
		wordsCount[iterator] += 1;
	}
	let count = 0;
	for (const key in wordsFrequency) {
		let hasBroken = false;
		for (const childKey in wordsFrequency[key]) {
			if (!charsFrequency[childKey]) {
				hasBroken = true;
				break;
			}
			if (charsFrequency[childKey] < wordsFrequency[key][childKey]) {
				hasBroken = true;
				break;
			}
		}
		if (!hasBroken) {
			count += wordsCount[key] * key.length;
		}
	}
	return count;
};

