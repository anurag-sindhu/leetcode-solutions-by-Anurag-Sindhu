var minCost = function (startPos, homePos, rowCosts, colCosts) {
    let minCost = 0;
    const rowCostsSum = [];
    const colCostsSum = [];
    const startRow = startPos[0];
    const startCol = startPos[1];
    const homeRow = homePos[0];
    const homeCol = homePos[1];
    for (const element of rowCosts) {
        rowCostsSum.push(element + (rowCostsSum[rowCostsSum.length - 1] || 0));
    }
    for (const element of colCosts) {
        colCostsSum.push(element + (colCostsSum[colCostsSum.length - 1] || 0));
    }
    if (homeRow > startRow) {
        minCost += rowCostsSum[homeRow] - rowCostsSum[startRow];
    } else if (homeRow < startRow) {
        minCost += rowCostsSum[startRow - 1] - (rowCostsSum[homeRow - 1] || 0);
    }
    if (homeCol > startCol) {
        minCost += colCostsSum[homeCol] - colCostsSum[startCol];
    } else if (homeCol < startCol) {
        minCost += colCostsSum[startCol - 1] - (colCostsSum[homeCol - 1] || 0);
    }
    return minCost;
};

